function detectBrowserLocale(): Locale {
  const saved = localStorage.getItem('locale') as Locale;
  if (saved && saved in ({'zh-CN':1,'en':1,'ja':1,'ko':1,'fi':1,'tr':1,'el':1,'id':1,'sl':1,'uk':1} as Record<string, number>)) return saved;
  const browser = navigator.language || 'en';
  if (browser.startsWith('zh')) return 'zh-CN';
  if (browser.startsWith('ja')) return 'ja';
  if (browser.startsWith('ko')) return 'ko';
  if (browser.startsWith('fi')) return 'fi';
  if (browser.startsWith('tr')) return 'tr';
  if (browser.startsWith('el')) return 'el';
  if (browser.startsWith('id')) return 'id';
  if (browser.startsWith('sl')) return 'sl';
  if (browser.startsWith('uk')) return 'uk';
  return 'en';
}

let currentLocale: Locale = detectBrowserLocale();

export function setLocale(locale: Locale) {
  currentLocale = locale;
  localStorage.setItem('locale', locale);
}

export function getLocale(): Locale {
  return currentLocale;
}

export function t(key: string, params?: Record<string, string | number>): string {
  let value = translations[currentLocale]?.[key] || translations['zh-CN']?.[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      value = value.replace(`{${k}}`, String(v));
    }
  }
  return value;
}

export function getLocaleLabel(locale: Locale): string {
  const labels: Record<Locale, string> = {
    'zh-CN': '简体中文',
    'en': 'English',
    'ja': '日本語',
    'ko': '한국어',
    'fi': 'Suomi',
    'tr': 'Türkçe',
    'el': 'Ελληνικά',
    'id': 'Bahasa Indonesia',
    'sl': 'Slovenščina',
    'uk': 'Українська',
  };
  return labels[locale] || locale;
}

export function getAllLocales(): Locale[] {
  return Object.keys(translations) as Locale[];
}
